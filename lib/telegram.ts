declare global {
  interface Window {
    Telegram?: {
      WebApp: {
        ready: () => void
        expand: () => void
        close: () => void
        MainButton: {
          text: string
          color: string
          textColor: string
          isVisible: boolean
          isActive: boolean
          show: () => void
          hide: () => void
          onClick: (callback: () => void) => void
        }
        BackButton: {
          isVisible: boolean
          show: () => void
          hide: () => void
          onClick: (callback: () => void) => void
        }
        initData: string
        initDataUnsafe: {
          user?: {
            id: number
            first_name: string
            last_name?: string
            username?: string
          }
        }
      }
    }
  }
}

export const telegram = typeof window !== "undefined" ? window.Telegram?.WebApp : null

export function initTelegramApp() {
  if (telegram) {
    telegram.ready()
    telegram.expand()
  }
}

export function getTelegramUser() {
  return telegram?.initDataUnsafe?.user
}
